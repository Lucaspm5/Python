frase = 'Lucas Emanuel'
# Vai do indice 0 até o indice 5, mas exclui o 5
print(frase[0:5])
# Igonara de 1 em 1
print(frase[0:5:2])
# Inicia do 0 e vai até o indice 5, lembrando que aparece apenas 5 elementos
print(frase[:5])
# Inicia do indice 6 e vai até o final da string
print(frase[6:])
# Inicia do indice 0, vai percorrer toda a string em 3 o 3 elemento é imprimido
print(frase[0::3])
